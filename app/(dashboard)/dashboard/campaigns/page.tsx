interface Campaign {
  id: string
  name: string
  normalizedName: string
  autoGenerated: boolean
  department: {
    id: string
    name: string
  }
  paymentCount: number
  totalAmount: number
  createdAt: string
  updatedAt: string
}

async function getCampaigns(): Promise<Campaign[]> {
  const campaigns = await prisma.campaignCategory.findMany({
    include: {
      department: {
        select: {
          id: true,
          name: true,
        },
      },
      _count: {
        select: {
          payments: true,
        },
      },
      payments: {
        select: {
          amount: true,
        },
      },
    },
    orderBy: [
      { autoGenerated: "asc" },
      { name: "asc" },
    ],
  })

  return campaigns.map((campaign) => {
    const totalAmount = campaign.payments.reduce((sum, payment) => {
      return sum + Number(payment.amount)
    }, 0)

    return {
      id: campaign.id,
      name: campaign.name,
      normalizedName: campaign.normalizedName,
      autoGenerated: campaign.autoGenerated,
      department: campaign.department,
      paymentCount: campaign._count.payments,
      totalAmount,
      createdAt: campaign.createdAt.toISOString(),
      updatedAt: campaign.updatedAt.toISOString(),
    }
  })
}

